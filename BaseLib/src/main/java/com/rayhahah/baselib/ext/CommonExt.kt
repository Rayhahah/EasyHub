fun <T, R> rNull(t: T, safe: (T) -> R, isNull: (T) -> R): R {
    if (t != null) {
        return safe(t)
    } else {
        return isNull(t)
    }
}

fun <T : Any> T.log(desc: String = this::class.java.simpleName, tag: String = "lzh") {
    RLog.e(tag, "$desc=${this}")
}

fun l(msg: Any, tag: String = "lzh") {
    RLog.e(tag, msg.toString())
}

fun lFile(msg: Any, tag: String = "lzh") {
    RLog.file(tag, msg.toString())
}


