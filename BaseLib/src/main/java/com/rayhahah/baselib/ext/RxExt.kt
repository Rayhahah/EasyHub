fun <T> Observable<T>.sub(next: (result: T) -> Unit, error: (t: Throwable) -> Unit = {}, complete: () -> Unit = {}): Disposable? {
    return this.observeOn(AndroidSchedulers.mainThread())
            .subscribeOn(Schedulers.io())
            .subscribe(next, error, complete)
}