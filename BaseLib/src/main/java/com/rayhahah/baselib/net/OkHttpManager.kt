class OkHttpManager {

    /**
     * 构建OkHttpClient
     */
    fun create(interceptor: Interceptor? = null): OkHttpClient {
        val builder = OkHttpClient.Builder()
                .addInterceptor(initLogInterceptor())
                .addNetworkInterceptor(StethoInterceptor())
                .connectTimeout(15, TimeUnit.SECONDS)
                .readTimeout(15, TimeUnit.SECONDS)
        if (interceptor != null) {
            builder.addNetworkInterceptor(interceptor)
        }
        return builder.build()
    }


    /**
     * 日志拦截器
     */
    private fun initLogInterceptor(): HttpLoggingInterceptor {
        val interceptor = HttpLoggingInterceptor()
        interceptor.level = HttpLoggingInterceptor.Level.BODY
        return interceptor
    }


    internal class HttpProgressInterceptor(private val mProgressListener: ProgressListener) : Interceptor {

        @Throws(IOException::class)
        override fun intercept(chain: Interceptor.Chain): Response {
            val originalResponse = chain.proceed(chain.request())
            return originalResponse
                    .newBuilder()
                    .body(ProgressResponseBody(originalResponse, mProgressListener))
                    .build()
        }
    }

    companion object {
        fun getInstance() = Holder.instance
    }

    object Holder {
        val instance = RetrofitFactory()
    }

}